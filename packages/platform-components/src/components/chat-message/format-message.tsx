export const messageToSmile = (message: string) => {
    return message
        .split(' ')
        .map((e, i) => {
            if (e.startsWith('http')) {
                return (
                    <a
                        className={b('chat-message', 'link')}
                        href={e}
                        key={`${e}${i}`}
                        rel="noreferrer"
                        target="_blank"
                    >
                        {e}
                    </a>
                );
            }

            if (e.startsWith('@')) {
                // TODO: redirect to user history
            }

            if (smiles.get(e)) {
                return (
                    <img
                        alt={e}
                        className={b('chat-message', 'text_smile')}
                        key={`${e}${i}`}
                        src={smiles.get(e)}
                        title={e}
                    />
                );
            }

            return e;
        }) //@ts-ignore
        .reduce((prev, curr) => [prev, ' ', curr]);
};
